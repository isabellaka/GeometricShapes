public class Point {
    private int x;
    private int y;

    //constructor
    Point(int x_coordinate, int y_coordinate) {
        x = x_coordinate;
        y = y_coordinate;
    }

    //getter for x
    int getX() {
        return x;
    }

    //getter for y
    int getY() {
        return y;
    }


}
